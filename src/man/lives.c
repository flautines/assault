#include <cpctelera.h>

u8 m_lives;
u8 m_need_draw;

/////////////////////////////////////////////////////////////////////////////
void manLivesInit()
{
	m_lives = 3;
	m_need_draw = 1;
}

/////////////////////////////////////////////////////////////////////////////
void manLivesUpdate()
{
	m_need_draw = 0;
}

/////////////////////////////////////////////////////////////////////////////
int manLivesGetNumber()
{
	return m_lives;
}

/////////////////////////////////////////////////////////////////////////////
int manLivesNeedDraw()
{
	return m_need_draw;
}
