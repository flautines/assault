#include <cpctelera.h>
#include "lives.h"

u8 m_lives;
u8 m_need_draw;

/////////////////////////////////////////////////////////////////////////////
void manLivesInit()
{
	m_lives = MAX_LIVES;
	m_need_draw = 1;
}

/////////////////////////////////////////////////////////////////////////////
void manLivesUpdate()
{
	m_need_draw = 0;
}

/////////////////////////////////////////////////////////////////////////////
void manLivesChange(i8 ammount)
{
	m_lives += ammount;
	m_need_draw = 1;
}

/////////////////////////////////////////////////////////////////////////////
int manLivesGetNumber()
{
	return m_lives;
}

/////////////////////////////////////////////////////////////////////////////
int  manLivesGetMax()
{
	return MAX_LIVES;
}

/////////////////////////////////////////////////////////////////////////////
int manLivesNeedDraw()
{
	return m_need_draw;
}
